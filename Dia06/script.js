// Operadores Aritméticos

let total = 6 + 2 // Adição
console.log(total)

total = 6 - 2 // Subtração
console.log(total)

total = 6 * 2 // Multiplicação
console.log(total)

total = 6 / 2 // Divisão
console.log(total)

total = 5 % 3 // Resto da Divisão
console.log(total)

total = 5
total++ // Incremento
console.log(total)

total = 5
total-- // Decremento
console.log(total)

total = 2 ** 3 // Exponencial
console.log(total)
